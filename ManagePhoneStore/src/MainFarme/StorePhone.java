    public ArrayList<InformationPhone> getListPage(int x, ArrayList<InformationPhone> list) {
        ArrayList<InformationPhone> lst = new ArrayList<InformationPhone>();
        int stt = 0;
        if (x < 2) {
            stt = -1;
        } else {
            stt = 6 + (x - 2) * 7;
        }
        if (list == null) {
            lst = null;
        } else if (list.size() > 0 && stt < list.size() - 1) {
//            Dialog.MessageInformation(null, list.size() + " -- " + stt);
            try {
                for (int i = stt + 1; i <= stt + 7; i++) {
                    lst.add(list.get(i));
                    if (i == list.size() - 1) {
                        break;
                    }
                }
            } catch (Exception e) {
                Dialog.MessageError(null, "Khng th ly danh sch trang " + x);
                return null;
            }
        } else {
            lst = null;
        }
        return lst;
    }
    
    public ArrayList<InformationPhone> getPage() {
        ArrayList<InformationPhone> t = null;
        if (!txttimkiem.getText().equalsIgnoreCase("")) {
            t = getListName(trang, txttimkiem.getText().trim());
        } else if (cboSapXep.getSelectedIndex() != 0) {
            int i = cboSapXep.getSelectedIndex();
            if (i == 1) {
                resetProduct();
                t = getListUp(trang, "Price");
                txttimkiem.setText("");
            } else if (i == 2) {
                resetProduct();
                t = getListDown(trang, "Price");
                txttimkiem.setText("");
            } else if (i == 3) {
                resetProduct();
                t = getListUp(trang, "Name");
                txttimkiem.setText("");
            } else if (i == 4) {
                resetProduct();
                t = getListDown(trang, "Name");
                txttimkiem.setText("");
            } else {
                resetProduct();
                t = getListProduct(trang);
                txttimkiem.setText("");
            }
        } else {
            t = getListProduct(trang);
        }
        return t;
    }
