    public ArrayList<InformationPhone> getListPriceDOWN() {
        ArrayList<InformationPhone> data = dao.SelectAll();
        if (data == null || data.size() == 0) {
            return null;
        } else {
            Comparator<InformationPhone> com = new Comparator<InformationPhone>() {
                @Override
                public int compare(InformationPhone o1, InformationPhone o2) {
                    Double x1 = o1.getPrice();
                    Double x2 = o2.getPrice();
                    return x2.compareTo(x1);
                }
            };
            Collections.sort(data, com);
            return data;
        }
    }
    
